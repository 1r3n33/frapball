void init_titlescreen(struct titlescreen_t *ts, UINT8 play_intro)
{
    UINT8 i;
    UINT8 j;
    UINT8 spr;

    UINT8 blank = 0x10;

    DISPLAY_OFF;

    if (play_intro)
    {
        ts->posx = 256U-4U;
        ts->posy = 128U;
        ts->timer_intro = 100U;
    }
    else
    {
        ts->posx = 256U-4U;
        ts->posy = 0U;
        ts->timer_intro = 0U;
    }

    ts->timer_selection = 0U;

    ts->timeout = TITLE_SCREEN_TIME_OUT;

    // Set background
    set_bkg_data(0, (frapball_tile_data_size/16), frapball_tile_data);
    for(i = 0; i < 32; i++)
        for(j = 0; j < 32; j++)
            set_bkg_tiles(i, j, 1, 1, &blank);
    set_bkg_tiles(1, 1, frapball_tile_map_width, frapball_tile_map_height, frapball_map_data);

    // Set sprites
    OBP0_REG = 0xD2; // custom palette
    OBP1_REG = 0xD2; // custom palette
    set_sprite_symbols();

    // ball as cursor
    set_sprite_data(0, 1, spr_ball_tile);

    spr = 0;

    for (i=0; i<STRLEN_1PLAYER; i++)
    {
        set_sprite_tile(spr, str_1player[i]);
        set_sprite_prop(spr, 0);
        spr++;
    }

    for (i=0; i<STRLEN_2PLAYERS; i++)
    {
        set_sprite_tile(spr, str_2players[i]);
        set_sprite_prop(spr, 0);
        spr++;
    }

    for (i=0; i<STRLEN_LINKGAME; i++)
    {
        set_sprite_tile(spr, str_linkgame[i]);
        set_sprite_prop(spr, 0);
        spr++;
    }

    ts->spr_cursor = spr;
    set_sprite_tile(spr, SPR_BALL);
    set_sprite_prop(spr, 0);

    ts->selection = 0;

    for (i=0; i<=spr; i++)
    {
        move_sprite(i, 0, 0);
    }

    init_sound();
    DISPLAY_ON;
}

void update_titlescreen(struct titlescreen_t *ts)
{
    UINT8 i;
    UINT8 x;
    UINT8 y;
    UINT8 jiggle;
    UINT8 spr;

    if (ts->timer_intro > 0)
    {
        if (ts->timer_intro > 36)
        {
            ts->posy-=2;
        }
        else if (ts->timer_intro > 32)
        {
            ts->posy+=2;
        }
        else if (ts->timer_intro > 28)
        {
            ts->posy-=2;
        }
        else
        {
            set_bkg_tiles(1, 16, 17, 1, copyright);
        }

        move_bkg(ts->posx, ts->posy);
        
        ts->timer_intro--;
    }
    else
    {
        set_bkg_tiles(1, 16, 17, 1, copyright);
        move_bkg(ts->posx, ts->posy);

        spr = 0;

        x = UI_SPRITES_POSX;
        y = UI_SPRITES_POSY;
        for (i=0; i<STRLEN_1PLAYER; i++)
        {
            jiggle = y;

            if (ts->selection == 0)
            {
                if ((ts->timer_selection>>2)%4 == 1)
                {
                    if (!i)       jiggle++;
                    else if (i&1) jiggle++;
                    else          jiggle--;
                }    
                if ((ts->timer_selection>>2)%4 == 3)
                {
                    if (!i)       jiggle--;
                    else if (i&1) jiggle--;
                    else          jiggle++;
                }    
            }

            move_sprite(spr, x, jiggle);
            spr++;
            x+=8;
        }

        x = UI_SPRITES_POSX;
        y = UI_SPRITES_POSY + 16;
        for (i=0; i<STRLEN_2PLAYERS; i++)
        {
            jiggle = y;
            if (ts->selection == 1)
            {
                if ((ts->timer_selection>>2)%4 == 1)
                {
                    if (!i)       jiggle++;
                    else if (i&1) jiggle++;
                    else          jiggle--;
                }    
                if ((ts->timer_selection>>2)%4 == 3)
                {
                    if (!i)       jiggle--;
                    else if (i&1) jiggle--;
                    else          jiggle++;
                }    
            }

            move_sprite(spr, x, jiggle);
            spr++;
            x+=8;
        }

        x = UI_SPRITES_POSX;
        y = UI_SPRITES_POSY + 32;
        for (i=0; i<STRLEN_LINKGAME; i++)
        {
            jiggle = y;
            if (ts->selection == 2)
            {
                if ((ts->timer_selection>>2)%4 == 1)
                {
                    if (i&1) jiggle++; // WRONG JIGGLE FOR SPACE CHAR ' ' BETWEEN LINK & GAME
                    else     jiggle--;
                }    
                if ((ts->timer_selection>>2)%4 == 3)
                {
                    if (i&1) jiggle--;
                    else     jiggle++;
                }    
            }

            move_sprite(spr, x, jiggle);
            spr++;
            x+=8;
        }

        move_sprite(spr, UI_SPRITES_POSX-16, UI_SPRITES_POSY+(16*ts->selection));

        ts->timer_selection++;
    }
}

void loop_titlescreen(struct titlescreen_t *ts)
{
    UINT8 i;
    UINT8 j;
    UINT8 pressed = joypad();

    while(1)
    {
        HIDE_WIN;
        SHOW_BKG;
        SHOW_SPRITES;

        update_titlescreen(ts);

        if (!pressed && !ts->timer_intro) // Activate joypad only when intro is done.
        {
            j = joypad();
            pressed = j;

            if (j & (J_START|J_A|J_B))
            {
                stop_sound();
                if (ts->selection == GAME_MODE_LINK)
                {
                    
                    lk_master_connect();
                    for (i=0; i<0xFF; i++)
                    {
                        delay(1);
                        if (lk_is_connected() == LK_MASTER)
                        {
                            return;
                        }
                    }
                    // failed to etablish a connection
                    lk_slave_connect();

                    init_sound();
                    ts->timeout = TITLE_SCREEN_TIME_OUT;
                }
                else
                {
                    return;
                }
            }

            if (j & J_SELECT)
            {
                ts->selection++;
                if (ts->selection > 2)
                {
                    ts->selection = 0;
                }
                ts->timeout = TITLE_SCREEN_TIME_OUT;
            }

            if (j & J_UP)
            {
                if (ts->selection > 0)
                {
                    ts->selection--;
                }
                ts->timeout = TITLE_SCREEN_TIME_OUT;
            }
            
            if (j & J_DOWN)
            {
                if (ts->selection < 2)
                {
                    ts->selection++;
                }
                ts->timeout = TITLE_SCREEN_TIME_OUT;
            }

            if (ts->selection == GAME_MODE_LINK)
            {
                if (lk_is_connected() == LK_SLAVE)
                {
                    return;
                }
                else
                {
                    lk_slave_connect();
                }
            }
            else
            {
                lk_disconnect();
            }
        }
        else
        {
            pressed = joypad();
        }

        play_sound();
		wait_vbl_done();

        ts->timeout--;
        if (ts->timeout == 0)
        {
          stop_sound();
          ts->selection = 3;
          return;
        }
	}	
}